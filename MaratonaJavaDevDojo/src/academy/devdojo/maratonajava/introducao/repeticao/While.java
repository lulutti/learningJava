package academy.devdojo.maratonajava.introducao.repeticao;

public class While {
    public static void main(String[] args) {
        // Contador de 0 a 10
        int count = 0;
        while(count <= 10){
            System.out.println(count);
            count++;
        }

        // Só executa se a inicialmente a condição for verdadeira
    }
}
