package academy.devdojo.maratonajava.introducao.arrays;

public class Arrays {
    public static void main(String[] args) {
        // Arrays fazem referência a x objetos na memória
       int [] idades = new int[3]; // As posições iniciam com valores padrões de cada tipo
        idades[0] = 25;
        idades[1] = 29;
        idades[2] = 36;

        System.out.println(idades[2]);
        System.out.println(idades[2]);
        System.out.println(idades[2]);
    }
}
