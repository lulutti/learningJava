package academy.devdojo.maratonajava.introducao.tiposprimitivos;

public class TiposPrimitivos {
    public static void main(String[] args) {
        // Tipos que guardam valores simples em memória
        // int, double, float, char, byte, short, long, boolean
        // nomes das variavéis em camelCase
        int integer = 1;
        long biggerNumber = 1000000l;
        double doubleNumber = 4000d;
        float floatNumber = 2500.0f;
        byte byteNumber = 127;
        short shortNumber = 32000;
        boolean isTrue = true;
        char oneChar = 'a';

        System.out.println(integer);
        System.out.println(oneChar);

    }
}
